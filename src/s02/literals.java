package s02;

public class literals {
	public static void main(String[] args) {
		int n = 1000;
		n = 0b01110110;
		long l = 1000000000000L;

		double dbl = 3.14;
		float fl = 3.14F;

		String s = "dsdfs";
		System.out.println(s.length());

		System.out.println(fl);

		long lo = 1000_1213_2244_22L;
		System.out.println(lo);

	}
}
